    /* 
    * Display an option pane for new game or load game when dead
    */
    private void deadOptionPane() {
        String[] options = { "New Game", "Load Game" };

        // Create a JOptionPane to prompt the selection
        JOptionPane optionPane = new JOptionPane(
                "Select an option:", 
                JOptionPane.QUESTION_MESSAGE,
                JOptionPane.DEFAULT_OPTION,
                null, // No default icon
                options,
                options[0]); // Default selection

        // Create a JDialog with the JOptionPane
        JDialog dialog = optionPane.createDialog("Game Menu");

        // Set dialog modality to block other input
        dialog.setModal(true);

        // Prevent closing the dialog with the X button
        dialog.setDefaultCloseOperation(JDialog.DO_NOTHING_ON_CLOSE);
        dialog.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(java.awt.event.WindowEvent windowEvent) {
                // Call the function again to reopen the dialog
                deadOptionPane(); 
            }
        });

        // Show the dialog
        dialog.setVisible(true);

        // Retrieve the selected option
        Object selectedValue = optionPane.getValue();
        if (selectedValue != null) {
            gameManager.stopPeriodicGameplay();
            // Check which option was selected
            if (selectedValue.equals("New Game")) {
                switchPanel("CharacterSelection");
            } else if (selectedValue.equals("Load Game")) {
                switchPanel("LoadGame");
            }
        }
    }
    
    /**
     * Plays music for the game based on the provided file path.
     *
     * @param filePath The path to the audio file to be played.
     */
    private void playMusic(String filePath) {
        try {
            if (!filePath.equals(currentClipPath)) { // Check if we are on the same audio file
                currentClipPath = filePath; // Update currentClipPath if switched audio files
                if (currentClip != null && currentClip.isRunning()) {
                    currentClip.stop(); // Stop the current playing audio
                    currentClip.close(); // Close the current clip
                }
                AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(new File(getClass().getResource(filePath).toURI()));
                currentClip = AudioSystem.getClip();
                currentClip.open(audioInputStream); // Open the new audio file
                currentClip.loop(Clip.LOOP_CONTINUOUSLY); // Loop indefinitely
                currentClip.start(); // Start playing the new audio
            }
        } catch (Exception e) {
            e.printStackTrace(); // Print any exceptions that occur
        }
    }

